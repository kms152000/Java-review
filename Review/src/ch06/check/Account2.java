package ch06.check;

public class Account2 {
	private String an;
	private String on;
	private int ba;
	
	public Account2 (String an, String on, int ba) {
		this.an = an;
		this.on = on;
		this.ba = ba;
	}
	
	public String getAn() {
		return an;
	}
	public void setAn(String an) {
		this.an = an;
	}
	public String getOn() {
		return on;
	}
	public void setOn(String on) {
		this.on = on;
	}
	public int getBa() {
		return ba;
	}
	public void setBa(int ba) {
		this.ba = ba;
	}

}
